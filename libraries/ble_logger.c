uint32_t ble_log_print(const char* format, ...) {
	char string[32];
	
	va_list arglist;
	va_start(arglist,format);
	uint16_t len = vsprintf(string,format,arglist);
	va_end(arglist);
	
	string[len++] = 0;
	
	uint32_t err_code;

	uint8_t i = 0;
	while(string[i] != 0) {
		ble_log( (uint8_t) string[i++]);
	}
	ble_log(0);

	return err_code;
}

